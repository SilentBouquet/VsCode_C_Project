//逻辑运算
//逻辑运算是对逻辑量进行的运算，结果只有0或1
//逻辑量是关系运算或逻辑运算的结果
//非：！；与：&&；或：||；
//优先级上：!>&&>||
//短路：逻辑运算是自左向右进行的，如果左边的结果已经能够决定结果，就不会做右边的计算
#include <stdio.h>
int main()
{
    char c = 'G';
    printf("%d\n",c>='A'&&c<='Z');//判断字符是否是大写字母
    int a = -1;
    if (a>0 && a++>1)
    {
        printf("ok\n");
    }
    printf("%d\n",a);
    return 0;
}